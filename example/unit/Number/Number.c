/*
 * Determines whether the specified number is odd, or not.
 */
bool example_Number_isOdd(int32_t number) {
    return (number % 2) == 1;
}

/*
 * Determines whether the specified number is odd, or not.
 */
bool example_Number_isEven(int32_t number) {
    return (number % 2) == 0;
}